def create_korean_query_rewrite_prompt(query: str) -> str:
    """
    사용자 질문을 4가지 방식(일반 검색, 키워드, 유사 답변, 핵심 내용)으로
    재작성하도록 지시하는 한국어 프롬프트를 생성합니다.

    Args:
        query (str): 재작성할 사용자의 원본 질문입니다.

    Returns:
        str: AI 모델에 전달할 수 있도록 완성된 프롬프트 문자열입니다.
    """
    prompt = f"""### 지시사항 ###
당신은 답변을 제공하기 위해 인터넷 검색을 통해 관련 콘텐츠를 찾아야 하는 사용자의 질문을 받게 됩니다. 현재 다음과 같은 네 가지 재작성 방법이 있습니다: 일반 검색 재작성, 키워드 재작성, 유사 답변 재작성, 핵심 내용 추출. 이 네 가지 재작성 방법을 적용하여 질문을 다시 작성해주세요.

### 일반 검색 재작성 (General Search Rewriting) ###
질문을 인터넷 검색을 위한 일반적인 검색어로 재작성합니다.

### 키워드 재작성 (Keyword Rewriting) ###
질문에서 모든 키워드를 추출하고 쉼표로 구분하여 원본 질문의 정보량을 보존합니다.

### 유사 답변 재작성 (Pseudo-Answer Rewriting) ###
질문에 대한 답변을 생성하고, 그 답변을 사용하여 검색 엔진의 실제 답변과 일치시킵니다.

### 핵심 내용 추출 (Core Content Extraction) ###
원본 질문의 정보량을 줄이고 가장 핵심적인 내용만 추출합니다. 재작성된 검색어는 키워드 재작성보다 더 간결해야 합니다.

### 예시 ###
질문: 제1차 세계 대전을 공식적으로 종식시킨 휴전 협정이 체결된 장소는 어느 도시였습니까?
결과:
  + 일반 검색 재작성: 제1차 세계 대전 휴전 협정이 체결된 도시
  + 키워드 재작성: 제1차 세계 대전, 휴전, 체결 장소
  + 유사 답변 재작성: 제1차 세계 대전을 종식시킨 휴전 협정은 콩피에뉴 시에서 체결되었습니다.
  + 핵심 내용 추출: 제1차 세계 대전 휴전 체결 도시

시작하세요! 다른 추가 내용 없이 최종 결과만 출력하세요. 관련 없는 다른 내용은 생성하지 마세요.

질문: {query}
결과:"""
    return prompt
if __name__ == "__main__":
    # --- 함수 사용 예시 ---
    user_question = "docker container 직접 들어가는 명령어를 줘"
    formatted_prompt = create_korean_query_rewrite_prompt(user_question)
    print(formatted_prompt)
